/**
 * Quét assets/illustrations/vocab và sinh src/data/vocabIllustrationMap.ts
 * (chỉ require các file đã có).
 *
 * Usage: node scripts/build-vocab-illustration-map.mjs
 */
import { readdirSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const vocabDir = path.join(root, 'assets', 'illustrations', 'vocab');
const outPath = path.join(root, 'src', 'data', 'vocabIllustrationMap.ts');

mkdirSync(vocabDir, { recursive: true });

const files = existsSync(vocabDir)
  ? readdirSync(vocabDir).filter((f) => /\.(png|webp|jpg|jpeg)$/i.test(f))
  : [];

const entries = files
  .map((f) => {
    const slug = f.replace(/\.(png|webp|jpg|jpeg)$/i, '');
    // Skip invalid JS identifier slugs that can't be used as bare keys — quote all keys.
    return { slug, file: f };
  })
  .sort((a, b) => a.slug.localeCompare(b.slug));

const lines = [
  '/** AUTO-GENERATED by scripts/build-vocab-illustration-map.mjs — do not edit by hand. */',
  "import type { ImageSourcePropType } from 'react-native';",
  '',
  'export const VOCAB_ILLUSTRATION_MAP: Record<string, ImageSourcePropType> = {',
  ...entries.map(
    (e) => `  ${JSON.stringify(e.slug)}: require('../../assets/illustrations/vocab/${e.file}'),`
  ),
  '};',
  '',
  'export function getVocabIllustrationSource(illust?: string | null): ImageSourcePropType | null {',
  '  if (!illust) return null;',
  '  return VOCAB_ILLUSTRATION_MAP[illust] ?? null;',
  '}',
  '',
];

writeFileSync(outPath, lines.join('\n'), 'utf8');
console.log(`Wrote ${entries.length} entries → ${outPath}`);
